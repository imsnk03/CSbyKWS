using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SeasonI.Episodes
{
	class Ep11_Switch1
	{

		public static void Switc1hMain()
		{
			Console.Write("Please enter a number[10-30]:");
			int var1 = int.Parse(Console.ReadLine());

			if (var1 == 10)
				Console.WriteLine("Entered Number is 10");
			else if (var1 == 20)
				Console.WriteLine("Entered Number is 20");
			else if (var1 == 30)
				Console.WriteLine("Entered Number is 30");
			else
				Console.WriteLine("Entered Number is not 10-30");



			//Switch Case replacing if condition
			switch (var1)
			{
				case 10:
					Console.WriteLine("Value is 10");
					break;
				case 20:
					Console.WriteLine("Value is 20");
					break;
				case 30:
					Console.WriteLine("Value is 30");
					break;
				default:
					Console.WriteLine("Value is not 10-30");
					break;
			}

			//Switch Case with no condition in block
			switch (var1)
			{
				case 10:				
				case 20:					
				case 30:
					Console.WriteLine("Value is {0}",var1);
					break;
				default:
					Console.WriteLine("Value is not 10-30");
					break;
			}





			Console.ReadKey();




		}
	}
}
